package com.ds.al.BinarySearch.TreeEntity;

public class BinarySearchTreeEntity {

	
	private String data;
	
	private BinarySearchTreeEntity right;
	
	private BinarySearchTreeEntity left;
	
	

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public BinarySearchTreeEntity getRight() {
		return right;
	}

	public void setRight(BinarySearchTreeEntity right) {
		this.right = right;
	}

	public BinarySearchTreeEntity getLeft() {
		return left;
	}

	public void setLeft(BinarySearchTreeEntity left) {
		this.left = left;
	}
	
	
	
	
	
}
