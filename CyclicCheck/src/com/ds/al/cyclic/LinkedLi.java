package com.ds.al.cyclic;

public class LinkedLi {
    private int data;
	private LinkedLi next;
	
	
	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}
	public LinkedLi getNext() {
		return next;
	}
	public void setNext(LinkedLi next) {
		this.next = next;
	}
	
}
